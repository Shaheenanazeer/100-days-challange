//  FUNCTION TYPES & ANNOTATIONS  
// Rectangle area calculation function
var calculateRectangleArea = function (length, width) {
    return length * width;
};
// Function call with valid arguments
var lengthRectAngle = 4;
var widthofRectAngle = 4;
var area = calculateRectangleArea(lengthRectAngle, widthofRectAngle);
console.log("The area of the rectangle is: ".concat(area));
